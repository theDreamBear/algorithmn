/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int maxAncestorDiff(TreeNode* root) {
        if (!root) return 0;
        int ans = 0;
        auto pre = [&](this auto&& dfs, TreeNode* node, int mx, int mm) {
            if (!node) {
                return;
            }
            mx = max(mx, node->val);
            mm = min(mm, node->val);
            ans = max(ans, mx - mm);
            dfs(node->left, mx, mm);
            dfs(node->right, mx, mm);
        };
        pre(root, INT_MIN, INT_MAX);
        return ans;
    }
};
